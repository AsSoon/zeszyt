package zeszyt;

public class Petelki {
	public static void main(String[] args) {

		for (int i=0; i<5; i++){
			System.out.println("Index i: " +i);
		}
		
		int j=0;
		while (j<5){
			System.out.println("Index j: " +j);
			j++;
		}
		
		int k=0;
		do{
			System.out.println("Index k: " +k);
			k++;
		}while (k<5);

	}
}
